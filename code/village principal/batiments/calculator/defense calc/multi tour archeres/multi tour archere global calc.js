let multi_tour_archere2levelmax = multi_tour_archere2_nv_max_hdv(hdvlevel);
let multi_tour_archere3levelmax = multi_tour_archere3_nv_max_hdv(hdvlevel);

export function calculerPourcentageNiveauxmulti_tour_archeres() {
    // Niveaux actuels
    const niveauxActuels = [
        Math.min(multi_tour_archere1level, multi_tour_archere1levelmax),
        Math.min(multi_tour_archere2level, multi_tour_archere2levelmax),
        Math.min(multi_tour_archere3level, multi_tour_archere3levelmax),
    ];

    // Niveaux max possibles selon HDV
    const niveauxMax = [
        multi_tour_archere1levelmax,
        multi_tour_archere2levelmax,
        multi_tour_archere3levelmax,
    ];

    // Calcul des totaux
    const totalMax = niveauxMax.reduce((a, b) => a + b, 0);
    const totalActuel = niveauxActuels.reduce((a, b) => a + b, 0);

    if (totalMax === 0) return 0; // Évite la division par zéro

    const progression = (totalActuel / totalMax) * 100;
    return parseFloat(progression.toFixed(2)); // Limite a 2 décimales
}

