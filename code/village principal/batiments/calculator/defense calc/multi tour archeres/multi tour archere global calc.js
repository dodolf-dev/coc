// Fonction pour mettre à jour la barre de progression
function updatemulti_tour_archereProgress() {
    // Recalculer le niveau de l'Hôtel de Ville
    hdvlevel = getHDVLevel();

    // Recalculer les niveaux des multi_tour_archeres
    multi_tour_archere1level = getmulti_tour_archereLevel(1);
    multi_tour_archere2level = getmulti_tour_archereLevel(2);
    multi_tour_archere3level = getmulti_tour_archereLevel(3);

    // Recalculer les niveaux maximums des multi_tour_archeres en fonction du niveau HDV
    multi_tour_archere1levelmax = multi_tour_archere1_nv_max_hdv(hdvlevel);
    multi_tour_archere2levelmax = multi_tour_archere2_nv_max_hdv(hdvlevel);
    multi_tour_archere3levelmax = multi_tour_archere3_nv_max_hdv(hdvlevel);
    // Calculer le pourcentage de progression
    const progression = calculerPourcentageNiveauxmulti_tour_archeres();

    // Mettre à jour la barre de progression et le texte
    const progressBar = document.getElementById('progress-multi_tour_archeres');
    const progressText = document.getElementById('progress-multi_tour_archeres-value');
    const progressTimeText = document.getElementById('progress-multi_tour_archeres-temps');
    const progressPriceText = document.getElementById('progress-multi_tour_archeres-prix-or');

    if (progressBar && progressText) {
        progressBar.value = progression;
        progressText.textContent = `${progression.toFixed(2)}%`; // Limiter à 2 décimales
    }
    if (progressTimeText) {
        const tempsRestant = globalcalculertempsRestantmulti_tour_archeres();
        progressTimeText.innerHTML = `${convertirSecondescompact(tempsRestant)} <img src="/coc/image/général/ressource/temps icone.png" alt="temps" class="icone-ressource">`;
    }
    if (progressPriceText) {
        const prixRestant = globalcalculerPrixRestantmulti_tour_archere();
        progressPriceText.innerHTML = `${formatPrix(prixRestant)} <img src="/coc/image/village principal/ressource/or village-p.jpg" alt="or" class="icone-ressource">`;
    }
}

// Ajouter des écouteurs d'événements pour détecter les changements
document.addEventListener('DOMContentLoaded', () => {
    const multi_tour_archereSelects = [
        document.getElementById('multi_tour_archere1'),
        document.getElementById('multi_tour_archere2'),
        document.getElementById('multi_tour_archere3'),
    ];

    // Ajouter un écouteur pour chaque multi_tour_archere
    multi_tour_archereSelects.forEach(select => {
        if (select) {
            select.addEventListener('change', updatemulti_tour_archereProgress);
        }
    });

    // Ajouter un écouteur pour le changement du niveau HDV
    const hdvSelect = document.getElementById('hdv');
    if (hdvSelect) {
        hdvSelect.addEventListener('change', updatemulti_tour_archereProgress);
    }

    // Initialiser la barre de progression au chargement
    updatemulti_tour_archereProgress();
});