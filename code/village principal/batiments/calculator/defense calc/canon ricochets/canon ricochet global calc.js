let canon_ricochet2levelmax = canon_ricochet2_nv_max_hdv(hdvlevel);
let canon_ricochet3levelmax = canon_ricochet3_nv_max_hdv(hdvlevel);

export function calculerPourcentageNiveauxcanon_ricochets() {
    // Niveaux actuels
    const niveauxActuels = [
        Math.min(canon_ricochet1level, canon_ricochet1levelmax),
        Math.min(canon_ricochet2level, canon_ricochet2levelmax),
        Math.min(canon_ricochet3level, canon_ricochet3levelmax),
    ];

    // Niveaux max possibles selon HDV
    const niveauxMax = [
        canon_ricochet1levelmax,
        canon_ricochet2levelmax,
        canon_ricochet3levelmax,
    ];

    // Calcul des totaux
    const totalMax = niveauxMax.reduce((a, b) => a + b, 0);
    const totalActuel = niveauxActuels.reduce((a, b) => a + b, 0);

    if (totalMax === 0) return 0; // Évite la division par zéro

    const progression = (totalActuel / totalMax) * 100;
    return parseFloat(progression.toFixed(2)); // Limite a 2 décimales
}

