// Fonction pour mettre à jour la barre de progression
function updatecanon_ricochetProgress() {
    // Recalculer le niveau de l'Hôtel de Ville
    hdvlevel = getHDVLevel();

    // Recalculer les niveaux des canon_ricochets
    canon_ricochet1level = getcanon_ricochetLevel(1);
    canon_ricochet2level = getcanon_ricochetLevel(2);
    canon_ricochet3level = getcanon_ricochetLevel(3);

    // Recalculer les niveaux maximums des canon_ricochets en fonction du niveau HDV
    canon_ricochet1levelmax = canon_ricochet1_nv_max_hdv(hdvlevel);
    canon_ricochet2levelmax = canon_ricochet2_nv_max_hdv(hdvlevel);
    canon_ricochet3levelmax = canon_ricochet3_nv_max_hdv(hdvlevel);
    // Calculer le pourcentage de progression
    const progression = calculerPourcentageNiveauxcanon_ricochets();

    // Mettre à jour la barre de progression et le texte
    const progressBar = document.getElementById('progress-canon_ricochets');
    const progressText = document.getElementById('progress-canon_ricochets-value');
    const progressTimeText = document.getElementById('progress-canon_ricochets-temps');
    const progressPriceText = document.getElementById('progress-canon_ricochets-prix-or');

    if (progressBar && progressText) {
        progressBar.value = progression;
        progressText.textContent = `${progression.toFixed(2)}%`; // Limiter à 2 décimales
    }
    if (progressTimeText) {
        const tempsRestant = globalcalculertempsRestantcanon_ricochets();
        progressTimeText.innerHTML = `${convertirSecondescompact(tempsRestant)} <img src="/coc/image/général/ressource/temps icone.png" alt="temps" class="icone-ressource">`;
    }
    if (progressPriceText) {
        const prixRestant = globalcalculerPrixRestantcanon_ricochet();
        progressPriceText.innerHTML = `${formatPrix(prixRestant)} <img src="/coc/image/village principal/ressource/or village-p.jpg" alt="or" class="icone-ressource">`;
    }
}

// Ajouter des écouteurs d'événements pour détecter les changements
document.addEventListener('DOMContentLoaded', () => {
    const canon_ricochetSelects = [
        document.getElementById('canon_ricochet1'),
        document.getElementById('canon_ricochet2'),
        document.getElementById('canon_ricochet3'),
    ];

    // Ajouter un écouteur pour chaque canon_ricochet
    canon_ricochetSelects.forEach(select => {
        if (select) {
            select.addEventListener('change', updatecanon_ricochetProgress);
        }
    });

    // Ajouter un écouteur pour le changement du niveau HDV
    const hdvSelect = document.getElementById('hdv');
    if (hdvSelect) {
        hdvSelect.addEventListener('change', updatecanon_ricochetProgress);
    }

    // Initialiser la barre de progression au chargement
    updatecanon_ricochetProgress();
});