// Fonction pour mettre à jour la barre de progression
function updateextracteur_elixir_noirProgress() {
    // Recalculer le niveau de l'Hôtel de Ville
    hdvlevel = getHDVLevel();

    // Recalculer les niveaux des extracteur_elixir_noirs
    extracteur_elixir_noir1level = getextracteur_elixir_noirLevel(1);
    extracteur_elixir_noir2level = getextracteur_elixir_noirLevel(2);
    extracteur_elixir_noir3level = getextracteur_elixir_noirLevel(3);


    // Recalculer les niveaux maximums des extracteur_elixir_noirs en fonction du niveau HDV
    extracteur_elixir_noir1levelmax = extracteur_elixir_noir1_nv_max_hdv(hdvlevel);
    extracteur_elixir_noir2levelmax = extracteur_elixir_noir2_nv_max_hdv(hdvlevel);
    extracteur_elixir_noir3levelmax = extracteur_elixir_noir3_nv_max_hdv(hdvlevel);
    // Calculer le pourcentage de progression
    const progression = calculerPourcentageNiveauxextracteur_elixir_noirs();

    // Mettre à jour la barre de progression et le texte
    const progressBar = document.getElementById('progress-extracteur_elixir_noirs');
    const progressText = document.getElementById('progress-extracteur_elixir_noirs-value');
    const progressTimeText = document.getElementById('progress-extracteur_elixir_noirs-temps');
    const progressPriceText = document.getElementById('progress-extracteur_elixir_noirs-prix-elixir');

    if (progressBar && progressText) {
        progressBar.value = progression;
        progressText.textContent = `${progression}%`; // Limiter à 2 décimales
    }
    if (progressTimeText) {
        const tempsRestant = globalcalculertempsRestantextracteur_elixir_noirs();
        progressTimeText.innerHTML = `${convertirSecondescompact(tempsRestant)} <img src="/coc/image/général/ressource/temps icone.png" alt="temps" class="icone-ressource">`;
    }
    if (progressPriceText) {
        const prixRestant = globalcalculerPrixRestantextracteur_elixir_noir();
        progressPriceText.innerHTML = `${formatPrix(prixRestant)} <img src="/coc/image/village principal/ressource/or village-p.jpg" alt="or" class="icone-ressource">`;
    }
}

// Ajouter des écouteurs d'événements pour détecter les changements
document.addEventListener('DOMContentLoaded', () => {
    const extracteur_elixir_noirSelects = [
        document.getElementById('extracteur_elixir_noir1'),
        document.getElementById('extracteur_elixir_noir2'),
        document.getElementById('extracteur_elixir_noir3'),
    ];

    // Ajouter un écouteur pour chaque extracteur_elixir_noir
    extracteur_elixir_noirSelects.forEach(select => {
        if (select) {
            select.addEventListener('change', updateextracteur_elixir_noirProgress);
        }
    });

    // Ajouter un écouteur pour le changement du niveau HDV
    const hdvSelect = document.getElementById('hdv');
    if (hdvSelect) {
        hdvSelect.addEventListener('change', updateextracteur_elixir_noirProgress);
    }

    // Initialiser la barre de progression au chargement
    updateextracteur_elixir_noirProgress();
});