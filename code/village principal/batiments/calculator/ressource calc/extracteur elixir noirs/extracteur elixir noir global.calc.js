export function calculerPourcentageNiveauxextracteur_elixir_noirs() {
    // Niveaux actuels
    const niveauxActuels = [
        Math.min(extracteur_elixir_noir1level, extracteur_elixir_noir1levelmax),
        Math.min(extracteur_elixir_noir2level, extracteur_elixir_noir2levelmax),
        Math.min(extracteur_elixir_noir3level, extracteur_elixir_noir3levelmax),
    ];

    // Niveaux max possibles selon HDV
    const niveauxMax = [
        extracteur_elixir_noir1levelmax,
        extracteur_elixir_noir2levelmax,
        extracteur_elixir_noir3levelmax,
    ];

    // Calcul des totaux
    const totalMax = niveauxMax.reduce((a, b) => a + b, 0);
    const totalActuel = niveauxActuels.reduce((a, b) => a + b, 0);

    if (totalMax === 0) return 0; // Évite la division par zéro

    const progression = (totalActuel / totalMax) * 100;
    return parseFloat(progression.toFixed(2)); // Limite a 2 décimales
}

